REM ***** CREACI�N DE TABLAS *****

Create TABLE   EMPLEADOS(
IDEMPLEADO NUMBER(3) CONSTRAINT PKEMPLEADOS PRIMARY KEY,
NOMBRE   VARCHAR2(50),
FNACIMIENTO DATE,
IDOFICINA  NUMBER(2),
PUESTO VARCHAR2(25),
FCONTRATO DATE,
JEFE NUMBER (3) CONSTRAINT fk_JEFE REFERENCES EMPLEADOS(IDEMPLEADO),
MOVIL CHAR(9),
CUOTA NUMBER(10,2),
VENTAS NUMBER(10,2)
);


Create TABLE  OFICINAS(
IDOFICINA  NUMBER(2) CONSTRAINT PKOFICINAS PRIMARY KEY,
DIRECCION VARCHAR2 (50),
CPOSTAL CHAR(5),
CIUDAD   VARCHAR2 (15),
REGION  VARCHAR2(10),
TELEFONO CHAR(9),
DIRECTOR   NUMBER(3) CONSTRAINT FK_DIRECTOR REFERENCES EMPLEADOS(IDEMPLEADO),
OBJETIVO  NUMBER(10),
VENTAS    NUMBER(10,2)
);	

Alter TABLE EMPLEADOS ADD CONSTRAINT FK_OFICINA FOREIGN KEY (IDOFICINA) REFERENCES OFICINAS(IDOFICINA);

CREATE TABLE CLIENTES(
IDCLIENTE NUMBER(4) CONSTRAINT PKCLIENTES PRIMARY KEY,
NOMBRE VARCHAR2(25),
FNACIMIENTO DATE,
FALTA DATE,
REPRESENTANTE NUMBER(3),
CONSTRAINT FKCEMPLEADOS FOREIGN KEY (REPRESENTANTE) REFERENCES EMPLEADOS(IDEMPLEADO)
);

CREATE TABLE  PRODUCTOS(
IDFABRICANTE VARCHAR2(5),
IDPRODUCTO   VARCHAR2(5),
DESCRIPCION  VARCHAR2(50), 
PUNITARIO   NUMBER(10,2),
STOCK NUMBER(5), 
NIVELNUEVOPEDIDO NUMBER(5),
CONSTRAINT PKPRODUCTOS PRIMARY KEY (IDFABRICANTE,IDPRODUCTO)
);

CREATE TABLE  PEDIDOS(
CODIGO NUMBER(3) CONSTRAINT PKPEDIDOS PRIMARY KEY,
NUMPEDIDO NUMBER(6),
FPEDIDO DATE,
FENVIO DATE,
IDCLIENTE NUMBER(4),
IDVENDEDOR NUMBER(3),
CONSTRAINT FKPCLIENTES FOREIGN KEY (IDCLIENTE) REFERENCES CLIENTES(IDCLIENTE),
CONSTRAINT FKPEMPLEADOS FOREIGN KEY (IDVENDEDOR) REFERENCES EMPLEADOS(IDEMPLEADO)
);

CREATE TABLE LINEAS_PEDIDOS(
CODIGO NUMBER(3),
FABRICANTE   VARCHAR2(5),
PRODUCTO   VARCHAR2(5),
PUNITARIO NUMBER(10,2),
CANTIDAD NUMBER(4),
CONSTRAINT PKLPEDIDOS PRIMARY KEY (CODIGO,FABRICANTE,PRODUCTO),
CONSTRAINT FKPEPRODUCTOS FOREIGN KEY (FABRICANTE,PRODUCTO) REFERENCES PRODUCTOS(IDFABRICANTE,IDPRODUCTO)
);


