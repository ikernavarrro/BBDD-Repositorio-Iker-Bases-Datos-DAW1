REM ***** CREaciON DE TABLAS *****

Create TABLE   DEPT(
DEPTNO NUMBER(2) PRIMARY KEY,
DNAME VARCHAR2(10) NOT NULL,
LOC VARCHAR2(8) NOT NULL
);


Create TABLE  EMP(
ENO NUMBER(4) PRIMARY KEY,
ENAME VARCHAR2 (6),
JOB VARCHAR2(9),
MGR NUMBER(4),
HIREDATE DATE,
SAL NUMBER(4),
COMM NUMBER(4) ,
DEPTNO NUMBER(2) CONSTRAINT FK_DEPT REFERENCES DEPT(DEPTNO)
);	

Alter table EMP ADD CONSTRAINT FK_SUPERIOR FOREIGN KEY (MGR) REFERENCES EMP(ENO);


CREATE TABLE   SALGRADE(
GRADE NUMBER(1) PRIMARY KEY,
LOSAL NUMBER(4),
HISAL NUMBER(4)
);



