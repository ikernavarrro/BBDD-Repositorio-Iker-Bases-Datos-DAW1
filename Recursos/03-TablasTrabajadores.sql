REM ***TABLAS TRABAJADOR, EDIFICIO Y ASIGNACION.***
REM ** BORRADO DE TABLAS *****

drop table TRABAJADOR cascade constraints;
drop table EDIFICIO cascade constraints;
drop table ASIGNACION cascade constraints;



REM ***** CREaciON DE TABLAS *****

Create TABLE   TRABAJADOR(
ID_T   NUMBER(4) PRIMARY KEY,
NOMBRE    VARCHAR2(20) NOT NULL,
TARIFA NUMBER(4,2) NOT NULL,
OFICIO  VARCHAR2(15) NOT NULL,
ID_SUPV NUMBER(4)
);

aLTER TABLE TRABAJADOR ADD CONSTRAINT fk_SUPERVISOR foreign key (ID_SUPV) REFERENCES TRABAJADOR (ID_T);

Create TABLE  EDIFICIO(
ID_E  NUMBER(3) PRIMARY KEY,
DIR   VARCHAR2 (15) NOT NULL,
TIPO  VARCHAR2(10) NOT NULL,
NIVEL_CALIDAD   NUMBER(1) NOT NULL,
CATEGORIA    NUMBER(1) NOT NULL
);	

CREATE TABLE   ASIGNACION(
ID_T   NUMBER(4) ,
ID_E  NUMBER(3),
FECHA_INICIO DATE,
NUM_DIAS  NUMBER(2),
CONSTRAINT PKASIGNACION PRIMARY KEY (ID_T,ID_E) 
);


